package com.funo.mobile.model;

public class ReportTwo {

	private String provinces;
	private String provincesCode;
	private String touristcount;
	private String cityId;
	private String date;
	
	
	public String getProvinces() {
		return provinces;
	}
	public void setProvinces(String provinces) {
		this.provinces = provinces;
	}
	public String getProvincesCode() {
		return provincesCode;
	}
	public void setProvincesCode(String provincesCode) {
		this.provincesCode = provincesCode;
	}
	public String getTouristcount() {
		return touristcount;
	}
	public void setTouristcount(String touristcount) {
		this.touristcount = touristcount;
	}
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
